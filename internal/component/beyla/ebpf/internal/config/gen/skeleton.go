//go:build ignore

package main

import (
	"fmt"
	"strings"
)

// generateSampleConfig walks the Arguments type and emits a full beyla.ebpf
// config skeleton: every block and attribute, with placeholder values and
// (best-effort) schema descriptions as comments.
//
// Values are placeholders, NOT Beyla defaults — the schema carries no defaults.
// It also lists deprecated and current fields together, so it's a reference of
// every available option, not a runnable/idiomatic config.
func generateSampleConfig(sc *schemaDoc) []byte {
	desc := buildDescriptions(sc)

	var sb strings.Builder
	sb.WriteString("// Code generated by internal/component/beyla/ebpf/internal/config/gen; DO NOT EDIT.\n")
	sb.WriteString("//\n")
	sb.WriteString("// Reference skeleton of every beyla.ebpf option. Values are placeholders, not\n")
	sb.WriteString("// defaults; deprecated and current fields are shown together.\n\n")
	sb.WriteString("beyla.ebpf \"example\" {\n")
	emitConfigFields(&sb, "\t", "Arguments", desc, map[string]bool{})
	sb.WriteString("}\n")

	return []byte(sb.String())
}

// buildDescriptions maps "<GoType>.<alloyKey>" → schema description. Best-effort:
// hand-written types with no 1:1 schema def (e.g. Metrics, Filters) have none.
func buildDescriptions(sc *schemaDoc) map[string]string {
	m := map[string]string{}
	record := func(goType string, props map[string]*schemaProp) {
		for schemaKey, prop := range props {
			if prop.Description != "" {
				m[goType+"."+aliasField(schemaKey)] = prop.Description
			}
		}
	}
	for defName, def := range sc.Defs {
		record(schemaGoName(defName), resolveProps(sc.Defs, def))
	}
	record("Arguments", sc.Properties)
	return m
}

// emitConfigFields writes every field of goType as an Alloy block or attribute.
// visited breaks reference cycles; it's cleared on return so a type may legitimately
// recur in sibling branches (e.g. a sampler block under both traces and a service).
func emitConfigFields(sb *strings.Builder, indent, goType string, desc map[string]string, visited map[string]bool) {
	fields := allStructs[goType]
	if fields == nil || visited[goType] {
		return
	}
	visited[goType] = true
	defer delete(visited, goType)

	for _, key := range sortedFieldKeys(fields) {
		fd := fields[key]
		if d := desc[goType+"."+key]; d != "" {
			fmt.Fprintf(sb, "%s// %s\n", indent, oneLine(d))
		}
		if isBlockType(fd.TypeStr) {
			elem := fd.TypeStr
			if e, ok := namedSlices[fd.TypeStr]; ok {
				elem = e // a repeated block: emit one example element
			}
			fmt.Fprintf(sb, "%s%s {\n", indent, key)
			emitConfigFields(sb, indent+"\t", elem, desc, visited)
			fmt.Fprintf(sb, "%s}\n", indent)
		} else {
			fmt.Fprintf(sb, "%s%s = %s\n", indent, key, placeholderValue(fd.TypeStr))
		}
	}
}

func placeholderValue(ts string) string {
	switch ts {
	case "bool", "*bool":
		return "false"
	case "int", "*int":
		return "0"
	case "time.Duration", "*time.Duration":
		return `"0s"`
	case "[]string":
		return "[]"
	case "map[string]string":
		return "{}"
	default: // string and anything unrecognized
		return `""`
	}
}

func oneLine(s string) string {
	return strings.Join(strings.Fields(s), " ")
}
